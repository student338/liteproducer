def _latin1_safe(text: str) -> str:
    """Replace characters not representable in latin-1 with ASCII equivalents."""
    replacements = {
        "\u2018": "'", "\u2019": "'", "\u201c": '"', "\u201d": '"',
        "\u2013": "-", "\u2014": "--", "\u2026": "...", "\u2022": "*",
    }
    for src, dst in replacements.items():
        text = text.replace(src, dst)
    return text.encode("latin-1", errors="replace").decode("latin-1")


class BookPDF(FPDF):
    def __init__(self, title: str, author: str = "liteproducer"):
        super().__init__()
        self.book_title = _latin1_safe(title)
        self.book_author = author
        self.set_auto_page_break(auto=True, margin=20)

    def header(self):
        self.set_font("Helvetica", "I", 9)
        self.set_text_color(120, 120, 120)
        self.cell(0, 8, self.book_title, align="C")
        self.ln(4)
        self.set_draw_color(200, 200, 200)
        self.line(self.l_margin, self.get_y(), self.w - self.r_margin, self.get_y())
        self.ln(4)

    def footer(self):
        self.set_y(-15)
        self.set_font("Helvetica", "I", 9)
        self.set_text_color(120, 120, 120)
        self.cell(0, 10, f"Page {self.page_no()}", align="C")


def build_pdf(title: str, chapters: list[dict]) -> str:
    """Render chapters list to a PDF file and return its path."""
    pdf = BookPDF(title)
    pdf.set_title(title)

    # Cover page
    pdf.add_page()
    pdf.set_font("Helvetica", "B", 28)
    pdf.set_text_color(30, 30, 30)
    pdf.ln(40)
    pdf.multi_cell(0, 12, _latin1_safe(title), align="C")
    pdf.ln(8)
    pdf.set_font("Helvetica", "", 14)
    pdf.set_text_color(80, 80, 80)
    pdf.cell(0, 8, f"Generated by liteproducer  -  {datetime.now().strftime('%B %d, %Y')}", align="C")

    for ch in chapters:
        pdf.add_page()
        # Chapter heading
        pdf.set_font("Helvetica", "B", 16)
        pdf.set_text_color(30, 30, 30)
        pdf.ln(4)
        pdf.multi_cell(0, 10, _latin1_safe(ch.get("heading", "Chapter")), align="L")
        pdf.ln(4)
        pdf.set_draw_color(60, 100, 200)
        pdf.set_line_width(0.5)
        pdf.line(pdf.l_margin, pdf.get_y(), pdf.w - pdf.r_margin, pdf.get_y())
        pdf.ln(6)
        # Body
        pdf.set_font("Helvetica", "", 11)
        pdf.set_text_color(30, 30, 30)
        body = ch.get("text", "")
        for para in body.split("\n"):
            para = para.strip()
            if para:
                pdf.multi_cell(0, 6, _latin1_safe(para), align="J")
                pdf.ln(3)

    safe_title = re.sub(r"[^\w\- ]", "", title)[:60].strip().replace(" ", "_")
    filename = f"{safe_title}_{uuid.uuid4().hex[:6]}.pdf"
    path = os.path.join(BOOKS_DIR, filename)
    pdf.output(path)
    return filename


