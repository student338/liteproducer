app = Flask(__name__)
BOOKS_DIR = os.path.join(os.path.dirname(__file__), "books")
os.makedirs(BOOKS_DIR, exist_ok=True)

# Active generation sessions keyed by session_id
sessions: dict[str, dict] = {}
sessions_lock = threading.Lock()


