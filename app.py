@app.route("/")
def index():
    return render_template("index.html")


@app.route("/api/start", methods=["POST"])
def start_generation():
    data = request.get_json(force=True)
    if not data.get("endpoint"):
        return jsonify({"error": "API endpoint is required"}), 400

    session_id = uuid.uuid4().hex
    with sessions_lock:
        sessions[session_id] = {
            "config": data,
            "queue": queue.Queue(),
            "instructions": [],
            "cancelled": False,
        }

    thread = threading.Thread(target=generate_book, args=(session_id,), daemon=True)
    thread.start()

    return jsonify({"session_id": session_id})


@app.route("/api/instruct/<session_id>", methods=["POST"])
def add_instruction(session_id: str):
    data = request.get_json(force=True)
    instruction = (data.get("instruction") or "").strip()
    if not instruction:
        return jsonify({"error": "No instruction provided"}), 400

    with sessions_lock:
        s = sessions.get(session_id)
        if not s:
            return jsonify({"error": "Session not found"}), 404
        s["instructions"].append(instruction)

    return jsonify({"ok": True})


@app.route("/api/cancel/<session_id>", methods=["POST"])
def cancel_generation(session_id: str):
    with sessions_lock:
        s = sessions.get(session_id)
        if not s:
            return jsonify({"error": "Session not found"}), 404
        s["cancelled"] = True
    return jsonify({"ok": True})


@app.route("/api/events/<session_id>")
def event_stream(session_id: str):
    def generate():
        timeout = 600  # 10 minutes max
        deadline = time.time() + timeout
        while time.time() < deadline:
            with sessions_lock:
                s = sessions.get(session_id)
            if not s:
                yield "data: {}\n\n"
                break
            try:
                item = s["queue"].get(timeout=1)
                payload = json.dumps({"event": item["event"], "data": item["data"]})
                yield f"data: {payload}\n\n"
                if item["event"] == "done":
                    break
            except queue.Empty:
                yield ": keep-alive\n\n"

    return Response(
        generate(),
        mimetype="text/event-stream",
        headers={
            "Cache-Control": "no-cache",
            "X-Accel-Buffering": "no",
        },
    )


@app.route("/api/books")
def list_books():
    files = sorted(
        [f for f in os.listdir(BOOKS_DIR) if f.endswith(".pdf")],
        key=lambda f: os.path.getmtime(os.path.join(BOOKS_DIR, f)),
        reverse=True,
    )
    return jsonify({"books": files})


@app.route("/books/<path:filename>")
def download_book(filename: str):
    return send_from_directory(BOOKS_DIR, filename, as_attachment=True)


